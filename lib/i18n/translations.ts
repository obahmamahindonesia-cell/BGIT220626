export type Lang = 'id' | 'en'
export type TranslationKey = keyof typeof translations

function deepGet(obj: any, path: string[]): any {
  let current = obj
  for (const key of path) {
    if (current === undefined || current === null) return undefined
    current = current[key]
  }
  return current
}

export function getTranslation(key: string, lang: Lang, params?: Record<string, string | number>): string {
  const parts = key.split('.')
  const value = deepGet(translations, parts)

  if (!value) return key
  if (typeof value === 'string') return value
  if (typeof value === 'object' && value !== null) {
    const text = value[lang]
    if (text === undefined) return key
    if (params) {
      return text.replace(/\{(\w+)\}/g, (_: string, p: string) => String(params[p] ?? `{${p}}`))
    }
    return text
  }
  return key
}

export function getArrayTranslation(key: string, lang: Lang): Array<{ id: string; en: string }> {
  const parts = key.split('.')
  const value = deepGet(translations, parts)
  if (Array.isArray(value)) return value
  return []
}
